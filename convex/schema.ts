import { authTables } from "@convex-dev/auth/server";
import { defineSchema, defineTable } from "convex/server";
import { v } from "convex/values";

const roleEnum = v.union(
  v.literal("AD"),
  v.literal("NBD"),
  v.literal("AI-BOSS"),
  v.literal("COO"),
  v.literal("CFD"),
  v.literal("BUH"),
  v.literal("HOD"),
  v.literal("ADMIN"),
);

const paymentSplitValidator = v.object({
  splitNumber: v.number(),
  amountWithoutVat: v.number(),
  amountWithVat: v.optional(v.number()),
  vatRate: v.optional(v.number()),
  currencyRate: v.optional(v.number()),
  paidAt: v.number(),
  nextPaymentAt: v.optional(v.number()),
  remainingAmountWithoutVat: v.optional(v.number()),
  finplanCostIds: v.optional(v.array(v.string())),
  actorEmail: v.string(),
  actorName: v.optional(v.string()),
  createdAt: v.number(),
});

const schema = defineSchema({
  ...authTables,
  roles: defineTable({
    email: v.string(),
    roles: v.array(roleEnum),
    active: v.boolean(),
    isTest: v.boolean(),
    fullName: v.optional(v.string()),
    creatorTitle: v.optional(v.string()),
    hodDepartments: v.optional(v.array(v.string())),
    createdAt: v.number(),
    updatedAt: v.number(),
  })
    .index("by_email", ["email"])
    .index("by_active", ["active"]),
  requests: defineTable({
    requestCode: v.optional(v.string()),
    title: v.optional(v.string()),
    createdBy: v.id("users"),
    createdByEmail: v.string(),
    createdByName: v.optional(v.string()),
    originalCreatedBy: v.optional(v.id("users")),
    originalCreatedByEmail: v.optional(v.string()),
    originalCreatedByName: v.optional(v.string()),
    archivedAuthorTransferredAt: v.optional(v.number()),
    archivedAt: v.optional(v.number()),
    category: v.string(),
    amount: v.number(),
    amountWithVat: v.optional(v.number()),
    vatRate: v.optional(v.number()),
    currency: v.string(),
    fundingSource: v.string(),
    counterparty: v.optional(v.string()),
    paymentMethod: v.optional(v.string()),
    cfdTag: v.optional(v.string()),
    justification: v.string(),
    details: v.optional(v.string()),
    investmentReturn: v.optional(v.string()),
    clientName: v.string(),
    contacts: v.array(v.string()),
    relatedRequests: v.optional(v.array(v.string())),
    links: v.array(v.string()),
    attachmentCount: v.optional(v.number()),
    lastAttachmentName: v.optional(v.string()),
    financePlanLinks: v.optional(v.array(v.string())),
    incomingAmount: v.optional(v.number()),
    incomingAmountWithVat: v.optional(v.number()),
    incomingRatio: v.optional(v.number()),
    shipmentDate: v.optional(v.number()),
    shipmentMonth: v.optional(v.string()),
    specialists: v.optional(
      v.array(
        v.object({
          id: v.string(),
          name: v.string(),
          sourceType: v.optional(v.string()),
          department: v.optional(v.string()),
          hours: v.optional(v.number()),
          directCost: v.optional(v.number()),
          hodConfirmed: v.optional(v.boolean()),
          validationSkipped: v.optional(v.boolean()),
        }),
      ),
    ),
    requiredRoles: v.array(roleEnum),
    status: v.union(
      v.literal("draft"),
      v.literal("hod_pending"),
      v.literal("pending"),
      v.literal("approved"),
      v.literal("rejected"),
      v.literal("awaiting_payment"),
      v.literal("payment_planned"),
      v.literal("partially_paid"),
      v.literal("paid"),
      v.literal("closed"),
    ),
    previousClosedStatus: v.optional(
      v.union(v.literal("approved"), v.literal("paid")),
    ),
    isCanceled: v.optional(v.boolean()),
    canceledAt: v.optional(v.number()),
    approvalDeadline: v.optional(v.number()),
    neededBy: v.optional(v.number()),
    paidBy: v.optional(v.number()),
    awaitingPaymentAt: v.optional(v.number()),
    awaitingPaymentByEmail: v.optional(v.string()),
    awaitingPaymentByName: v.optional(v.string()),
    paidAt: v.optional(v.number()),
    paidByEmail: v.optional(v.string()),
    paidByName: v.optional(v.string()),
    actualPaidAmount: v.optional(v.number()),
    actualPaidAmountWithVat: v.optional(v.number()),
    paymentResidualAmount: v.optional(v.number()),
    paymentResidualAmountWithVat: v.optional(v.number()),
    plannedPaymentAmount: v.optional(v.number()),
    plannedPaymentAmountWithVat: v.optional(v.number()),
    paymentCurrencyRate: v.optional(v.number()),
    paymentPlannedAt: v.optional(v.number()),
    paymentPlannedByEmail: v.optional(v.string()),
    paymentPlannedByName: v.optional(v.string()),
    finplanCostIds: v.optional(v.array(v.string())),
    paymentSplits: v.optional(v.array(paymentSplitValidator)),
    approvalReminderSentAt: v.optional(v.number()),
    paymentReminderSentAt: v.optional(v.number()),
    closeReminderSentAt: v.optional(v.number()),
    submittedAt: v.optional(v.number()),
    createdAt: v.number(),
    updatedAt: v.number(),
  })
    .index("by_createdBy", ["createdBy"])
    .index("by_createdByEmail", ["createdByEmail"])
    .index("by_status", ["status"]),
  approvals: defineTable({
    requestId: v.id("requests"),
    role: roleEnum,
    status: v.union(
      v.literal("pending"),
      v.literal("approved"),
      v.literal("rejected"),
    ),
    comment: v.optional(v.string()),
    decidedAt: v.optional(v.number()),
    reviewerId: v.optional(v.id("users")),
    reviewerEmail: v.optional(v.string()),
  })
    .index("by_request", ["requestId"])
    .index("by_role", ["role"])
    .index("by_status", ["status"]),
  comments: defineTable({
    requestId: v.id("requests"),
    authorId: v.id("users"),
    authorEmail: v.string(),
    authorName: v.optional(v.string()),
    body: v.string(),
    parentId: v.optional(v.id("comments")),
    createdAt: v.number(),
    updatedAt: v.number(),
  })
    .index("by_request", ["requestId"])
    .index("by_parent", ["parentId"]),
  requestAttachments: defineTable({
    requestId: v.id("requests"),
    storageId: v.id("_storage"),
    fileName: v.string(),
    fileSize: v.optional(v.number()),
    contentType: v.optional(v.string()),
    uploadedByEmail: v.string(),
    uploadedByName: v.optional(v.string()),
    createdAt: v.number(),
  }).index("by_request", ["requestId"]),
  requestChangeLogs: defineTable({
    requestId: v.id("requests"),
    groupId: v.optional(v.string()),
    field: v.string(),
    fromValue: v.optional(v.string()),
    toValue: v.optional(v.string()),
    authorEmail: v.string(),
    authorName: v.optional(v.string()),
    triggeredRepeatApproval: v.optional(v.boolean()),
    groupSummary: v.optional(v.string()),
    createdAt: v.number(),
  }).index("by_request", ["requestId"]),
  requestTimelineEvents: defineTable({
    requestId: v.id("requests"),
    type: v.string(),
    title: v.string(),
    description: v.optional(v.string()),
    actorEmail: v.optional(v.string()),
    actorName: v.optional(v.string()),
    metadata: v.optional(v.string()),
    createdAt: v.number(),
  }).index("by_request", ["requestId"]),
  requestEmailLogs: defineTable({
    requestId: v.optional(v.id("requests")),
    emailType: v.string(),
    recipients: v.array(v.string()),
    subject: v.string(),
    status: v.union(v.literal("sent"), v.literal("failed")),
    error: v.optional(v.string()),
    createdAt: v.number(),
  }).index("by_request", ["requestId"]),
  presalesQuotas: defineTable({
    monthKey: v.string(),
    year: v.number(),
    month: v.number(),
    quota: v.number(),
    quotaWithVat: v.optional(v.number()),
    adjustedQuota: v.optional(v.number()),
    adjustedQuotaWithVat: v.optional(v.number()),
    vatRate: v.optional(v.number()),
    spent: v.number(),
    spentWithVat: v.optional(v.number()),
    updatedAt: v.number(),
  }).index("by_monthKey", ["monthKey"]),
  aiToolQuotas: defineTable({
    monthKey: v.string(),
    year: v.number(),
    month: v.number(),
    quota: v.number(),
    quotaWithVat: v.optional(v.number()),
    adjustedQuota: v.optional(v.number()),
    adjustedQuotaWithVat: v.optional(v.number()),
    vatRate: v.optional(v.number()),
    spent: v.number(),
    spentWithVat: v.optional(v.number()),
    updatedAt: v.number(),
  }).index("by_monthKey", ["monthKey"]),
  cfdTags: defineTable({
    name: v.string(),
    active: v.boolean(),
    createdAt: v.number(),
    updatedAt: v.number(),
  }).index("by_name", ["name"]),
  cfdQuotas: defineTable({
    monthKey: v.string(),
    year: v.number(),
    month: v.number(),
    quota: v.number(),
    quotaWithVat: v.optional(v.number()),
    adjustedQuota: v.optional(v.number()),
    adjustedQuotaWithVat: v.optional(v.number()),
    vatRate: v.optional(v.number()),
    spent: v.number(),
    spentWithVat: v.optional(v.number()),
    updatedAt: v.number(),
  }).index("by_monthKey", ["monthKey"]),
  cooQuotas: defineTable({
    monthKey: v.string(),
    year: v.number(),
    month: v.number(),
    quota: v.number(),
    quotaWithVat: v.optional(v.number()),
    adjustedQuota: v.optional(v.number()),
    adjustedQuotaWithVat: v.optional(v.number()),
    vatRate: v.optional(v.number()),
    spent: v.number(),
    spentWithVat: v.optional(v.number()),
    updatedAt: v.number(),
  }).index("by_monthKey", ["monthKey"]),
  requestCounters: defineTable({
    key: v.string(),
    nextNumber: v.number(),
    updatedAt: v.number(),
  }).index("by_key", ["key"]),
});

export default schema;
